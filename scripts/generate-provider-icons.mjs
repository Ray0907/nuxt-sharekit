import { readFile, writeFile } from 'node:fs/promises'
import { createRequire } from 'node:module'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const require_docs = createRequire(new URL('../docs-app/package.json', import.meta.url))
const path_simple = require_docs.resolve('@iconify-json/simple-icons/icons.json')
const path_lucide = require_docs.resolve('@iconify-json/lucide/icons.json')
const icons_simple = JSON.parse(await readFile(path_simple, 'utf8')).icons
const icons_lucide = JSON.parse(await readFile(path_lucide, 'utf8')).icons
const definitions_icon = {
	'simple-icons:facebook': ['simple', 'facebook'],
	'simple-icons:x': ['simple', 'x'],
	'simple-icons:linkedin': ['simple', 'linkedin'],
	'simple-icons:pinterest': ['simple', 'pinterest'],
	'simple-icons:reddit': ['simple', 'reddit'],
	'simple-icons:threads': ['simple', 'threads'],
	'simple-icons:bluesky': ['simple', 'bluesky'],
	'simple-icons:mastodon': ['simple', 'mastodon'],
	'simple-icons:tumblr': ['simple', 'tumblr'],
	'simple-icons:ycombinator': ['simple', 'ycombinator'],
	'simple-icons:whatsapp': ['simple', 'whatsapp'],
	'simple-icons:telegram': ['simple', 'telegram'],
	'simple-icons:line': ['simple', 'line'],
	'simple-icons:viber': ['simple', 'viber'],
	'lucide:mail': ['lucide', 'mail'],
	'lucide:message-square': ['lucide', 'message-square'],
	'simple-icons:sinaweibo': ['simple', 'sinaweibo'],
	'simple-icons:qzone': ['simple', 'qzone'],
	'simple-icons:vk': ['simple', 'vk'],
	'simple-icons:xing': ['simple', 'xing'],
	'simple-icons:instapaper': ['simple', 'instapaper'],
	'lucide:bookmark': ['lucide', 'bookmark'],
	'simple-icons:openai': ['simple', 'openai'],
	'simple-icons:claude': ['simple', 'claude'],
	'simple-icons:googlegemini': ['simple', 'googlegemini'],
	'simple-icons:perplexity': ['simple', 'perplexity'],
}

function getIconData(name_icon, definition_icon) {
	const [name_collection, name_source] = definition_icon
	const icons_collection = name_collection === 'simple' ? icons_simple : icons_lucide
	const icon_source = icons_collection[name_source]

	if (!icon_source) throw new Error(`Missing icon source for ${name_icon}`)

	return {
		viewBox: `0 0 ${icon_source.width || 24} ${icon_source.height || 24}`,
		body: icon_source.body,
	}
}

const entries_icon = Object.entries(definitions_icon).map(([name_icon, definition_icon]) => {
	const icon_data = getIconData(name_icon, definition_icon)
	return [name_icon, icon_data]
})
const source_generated = `/* This file is generated by scripts/generate-provider-icons.mjs. */
export interface ProviderIconData {
\tviewBox: string
\tbody: string
}

export const providerIcons: Readonly<Record<string, ProviderIconData>> = ${JSON.stringify(
	Object.fromEntries(entries_icon),
	null,
	'\t',
)}
`
const path_current = dirname(fileURLToPath(import.meta.url))
const path_output = resolve(
	path_current,
	'../packages/nuxt/src/runtime/provider-icons.ts',
)

await writeFile(path_output, source_generated)
